// You can increment variables in 2 ways

var myVar = 21;

// You can do it like this

myVar = myVar + 1;

console.log(myVar);
// or More effectively;
myVar = 21; //resetting for this example
myVar++;

console.log(myVar);

// Run this code by writing in the terminal `node PATH_TO_JS_FILE`

// for subtraction
myVar--;
console.log(myVar);

// You can add/subtract other numbers to myVar by using
// += and  -=

myVar += 12;

console.log(myVar);

myVar -= 12;

console.log(myVar);

// Similarly with multiplication/ division we can use *= and /=

myVar *= 2;
console.log(myVar);

myVar /= 7;
console.log(myVar);
